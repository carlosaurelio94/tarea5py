def areaTriangulo(base, altura):
    return base*altura/2

resultado = areaTriangulo(10, 5)
print('El area de un triangulo es: ', resultado, 'metros cuadrados')

def areaCirculo(radio):
    pi = 3.14
    return pi*radio**2

resultado =areaCirculo(5)
print('El area del circulo es: ', resultado, 'metros cuadrados')